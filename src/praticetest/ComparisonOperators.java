package praticetest;

public class ComparisonOperators {

	public static void main(String[] args) {
	/*List of comparison operators : == (equal to), != (Not equal to), < (Less than), 
	 >(Greater than), <= (Less than equal to), >= (Greater than equal to)
	 and returns value in boolean data type only : true or false
	 This operator is use to check the single condition*/ 
		int p = 11;
		int q = 15;
		System.out.println(p==q);
		System.out.println(p!=q);
		System.out.println(p<q);
		System.out.println(p>q);
		System.out.println(p<=q);
		System.out.println(p>=q);
	}

}
