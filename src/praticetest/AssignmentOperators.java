package praticetest;

public class AssignmentOperators {
	
	public static void main (String []args) {
		//Assignment operators:
		int l = 10;
		int m = 15;
		l+=m;
		System.out.println(l);
		
		int a = 10;
		int b = 12;
		b*=a;
		System.out.println(b);
		
		int c = 5;
		int d = 11;
		c-=d;
		System.out.println(c);
		
		int p = 100;
		int q = 5;		
		p/=q;
		System.out.println(p);
		
	}

}
